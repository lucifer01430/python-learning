name = 'Harsh'

print(name[0:3]) # here 0 to 3 means index 0,1,2 (not included index 3) so output will be Har
print(name[-2:]) # here -2 means start from index -2 and end at the end of the string so output will be sh

print(name[1:4]) # here 1 to 4 means index 1,2,3 (not included index 4) so output will be ars
print(name[-4:-1]) # here -4 to -1 means index -4,-3,-2 (not included index -1) so output will be ars
